namespace NStuff.WindowSystem
{
    /// <summary>
    /// Lists the size states of a <see cref="Window"/>
    /// </summary>
    public enum WindowSizeState
    {
        /// <summary>
        /// Indicates that the window is default sized.
        /// </summary>
        Normal,

        /// <summary>
        /// Indicates that the window is minimized.
        /// </summary>
        Minimized,

        /// <summary>
        /// Indicates that the window is maximized.
        /// </summary>
        Maximized
    }
}
