namespace NStuff.WindowSystem
{
    /// <summary>
    /// Lists the standard cursor shapes.
    /// </summary>
    public enum CursorShape
    {
        /// <summary>
        /// The default cursor.
        /// </summary>
        Arrow,

        /// <summary>
        /// The text editing cursor.
        /// </summary>
        IBeam,

        /// <summary>
        /// The crosshair cursor.
        /// </summary>
        Crosshair,
        
        /// <summary>
        /// The drag cursor.
        /// </summary>
        Hand,
        
        /// <summary>
        /// The left right resize cursor.
        /// </summary>
        HorizontalResize,
        
        /// <summary>
        /// The top bottom resize cursor.
        /// </summary>
        VerticalResize
    }
}
